class ConditionalStatements {
    public static void main(String args[])
    {
        int x = 4;
        if(x > 5 && x <= 40) {
            System.out.println("Hello");
        }
        else if(x > 3) {
            System.out.println("Yo");
        }
        else {
            System.out.println("Bye");
        }
        
    }
}